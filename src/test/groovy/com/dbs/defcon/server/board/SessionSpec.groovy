//@WebMvcTest(controllers = [SessionController])
class SessionSpec extends Specification {

	def "it should create a new session when create is called"() {
		given: "an HttpRequest with valid IP address and port"
		def SessionController controller = new SessionController()

		when: "called with a valid IP address"
		HttpServletRequest request = new MyHttpServletRequest()
		def sessionId = controller.createSession(request)
		
		then: "it should return a valid session id"
		sessionId != 23
	}
	
	@PendingFeature
	def "an open session should allow creation of a learning loop"() {
		given: "an open session"
		
		
		when: "a new question is posed"
		
		then: "it should allow multiple replies"
		
		when: "the question is closed"
		
		then: "it should return the results"
		1 == 0
	}
	
	@PendingFeature
	def "it should only allow one open question at a time"() {
		given: "an open session"
		
		and: "it has an open question"
		
		when: "a new question is posed"
		
		then: "the prior question should be closed"
		true == false	
	}
	
	def "it should track multiple answers to the open question"() {
		given: "an new session with an open question"
		def SessionController controller = new SessionController()
		HttpServletRequest request = new MyHttpServletRequest()
		def sessionId = Integer.valueOf( controller.createSession(request) )
		
		when: "a user answers with 5"
		controller.vote(sessionId, 5)
		
		and: "current data about the session is retrieved"
		def voteData = controller.getSessionData(sessionId)
		
		then: "the data should reflect the single vote"
		voteData.voteCount == 1
		voteData.voteLevel == 5
		voteData.votes.contains( new Vote(5) )
		voteData.votes.get(0).getLevel() == 5
		
		when: "another user answers with 3"
		controller.vote(sessionId, 3)
		
		and: "current data about the session is retrieved"
		voteData = controller.getSessionData(sessionId)
		
		then: "the data should show two votes"
		voteData.voteCount == 2
		
		and: "the voteLevel should be the average of the two votes (4)"
		voteData.voteLevel == 4
		
		and: "both votes should be recorded"
		voteData.votes.contains( new Vote(5) )
		voteData.votes.contains( new Vote(3) )
	}
}

